namespace Mohsen.PersianDateControls;

public enum PersianMonth
{
    /// <summary>
    /// Farvardin, The first month of the persian year, starts right after spring equinox. March-April
    /// </summary>
    فروردین = 1,

    /// <summary>
    /// Ordibehesht, The second month of the persian year, April-May
    /// </summary>
    اردیبهشت,

    /// <summary>
    /// Khordad, The third month of the persian year, May-June
    /// </summary>
    خرداد,

    /// <summary>
    /// Tir, The fourth month of the persian year, starts right after the longest day of the year. June-July
    /// </summary>
    تیر,

    /// <summary>
    /// Mordad, The fifth month of the persian year, July-August
    /// </summary>
    مرداد,

    /// <summary>
    /// Shahrivar, The sixth month of the persian year. August-September
    /// </summary>
    شهریور,

    /// <summary>
    /// Mehr, The seventh month of the persian year, starts right after autumn equinox. September-October
    /// </summary>
    مهر,

    /// <summary>
    /// Aban, The Eighth month of the persian year. October-November
    /// </summary>
    آبان,

    /// <summary>
    /// Azar, The ninth month of the persian year. November-December
    /// </summary>
    آذر,

    /// <summary>
    /// Dey, The tenth month of the persian year, starts right after the longest night of the year. December-January
    /// </summary>
    دی,

    /// <summary>
    /// Bahman, The eleventh month of the persian year. January-February
    /// </summary>
    بهمن,

    /// <summary>
    /// Esfand, The twelveth and the final month of the persian year. February-March
    /// </summary>
    اسفند
}