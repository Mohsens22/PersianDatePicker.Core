namespace Mohsen.PersianDateControls;

public enum PersianDayOfWeek
{
    /// <summary>
    /// Shanbeh, The first day of persian week, Saturday.
    /// </summary>
    شنبه = 6,

    /// <summary>
    /// Yek Shanbeh, The second day of persian week, Sunday.
    /// </summary>
    یکشنبه = 0,

    /// <summary>
    /// Do Shanbeh, The third day of persian week, Monday.
    /// </summary>
    دوشنبه,

    /// <summary>
    /// Se Shanbeh, The fourth day of persian week, Tuesday.
    /// </summary>
    ﺳﻪشنبه,

    /// <summary>
    /// Chahar Shanbeh, The fifth day of persian week, Wednsday.
    /// </summary>
    چهارشنبه,

    /// <summary>
    /// Panj Shanbeh, The sixth day of persian week, Thursday.
    /// </summary>
    پنجشنبه,

    /// <summary>
    /// Friday, The seventh day of persian week, Friday, weekend.
    /// </summary>
    جمعه
}